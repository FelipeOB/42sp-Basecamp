void	ft_sort_int_tab(int *tab, int size)
{
	int j;
	int x;
	int i;

	j = 1;
	while (j < size)
	{
		x = tab[j];
		i = j - 1;
		while (i >= 0 && tab[i] > x)
		{
			tab[i + 1] = tab[i];
			i--;
		}
		tab[i + 1] = x;
		j++;
	}
}
